    public void findSanPham(String tenSP){
        
        String sql = "SELECT * FROM SANPHAMCONLAI where TenSP like ?";
        
        try
        {
            conn = connectDB.connectSQLServer();
            pst = conn.prepareStatement(sql);
            pst.setString(1, "%" +tenSP+ "%");
            rs = pst.executeQuery();
            
            while(model.getRowCount()>0){
                model.removeRow(0);
            }
                SanPham sanPham = null;
            while(rs.next()){
                
                sanPham = new SanPham();
                
                    sanPham.setMaSP(rs.getString("MaSP"));
                    sanPham.setMaDM(rs.getString("MaDM"));
                    sanPham.setTenSP(rs.getString("TenSP"));
                    sanPham.setSoLuong(rs.getInt("SoLuong"));
                    sanPham.setDonGia(rs.getFloat("Dongia"));
                    sanPham.setMaNPPSP(rs.getString("MaNhaPPSS"));
                    sanPham.setThoiGianNhan(rs.getDate("Thoigiannhan"));
                    sanPham.setGhiChu(rs.getString("Ghichu"));
                    sanPham.setTongGia(rs.getFloat("Tonggia"));

                    
                    Vector row = new Vector();
                    row.add(sanPham.getMaSP());
                    row.add(sanPham.getMaDM());
                    row.add(sanPham.getTenSP());
                    row.add(sanPham.getSoLuong());
                    row.add(sanPham.getDonGia());
                    row.add(sanPham.getMaNPPSP());
                    row.add(sanPham.getThoiGianNhan());
                    row.add(sanPham.getGhiChu());
                    row.add(sanPham.getTongGia());


                model.addRow(row);
                 
            }
            this.tbbansanpham.setModel(model);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Error: " +e.getMessage());
        }
    }
