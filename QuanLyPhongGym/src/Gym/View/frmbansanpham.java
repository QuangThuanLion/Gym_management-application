    private void btBanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBanActionPerformed
        // Bán sản phẩm đang có
        if(this.txtmahoadon.getText().length()==0){
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập mã hóa đơn !","Thông báo",2);
                    this.txtmahoadon.requestFocus();
                    return;
        }
        if (this.txtsoluong.getText().length()==0) {
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập số lượng sản phẩm muốn bán","Thông báo",2);
                    this.txtsoluong.requestFocus();
                    return;
        }
        
        Pattern pattern = Pattern.compile("^[0-9]*$");
        Matcher matcher = pattern.matcher(this.txtsoluong.getText());
        if (!matcher.matches()) {
                    JOptionPane.showMessageDialog(null, "kí tự tại mục số lượng không phải là số!","Thông báo",2);
                    this.txtsoluong.requestFocus();
                    return;
        }
        if (this.txtgiaban.getText().length()==0) {
                    JOptionPane.showMessageDialog(null, "Giá bán không được để trống","Thông báo",2);
                    this.txtgiaban.requestFocus();
                    return;
        }
        
        Pattern pattern1 = Pattern.compile("^[0-9]*$");
        Matcher matcher1 = pattern1.matcher(this.txtgiaban.getText());
        if (!matcher1.matches()) {
                    JOptionPane.showMessageDialog(null, "kí tự tại mục giá bán không phải là số!","Thông báo",2);
                    this.txtgiaban.requestFocus();
                    return;
        }

        if (this.txttonggia.getText().length()==0) {
                    JOptionPane.showMessageDialog(null, "Tổng giá bán không được để trống","Thông báo",2);
                    this.txttonggia.requestFocus();
                    return;
        }
         
        Pattern pattern2 = Pattern.compile("^[0-9]*$");
        Matcher matcher2 = pattern2.matcher(this.txttonggia.getText());
        if (!matcher2.matches()) {
                    JOptionPane.showMessageDialog(null, "kí tự tại mục tổng giá không phải là số!","Thông báo",2);
                    this.txttonggia.requestFocus();
                    return;
        }
        if(this.cethanhtoan.isSelected()==false && this.ceconno.isSelected()==false){
                    JOptionPane.showMessageDialog(null,"Mời bạn chọn tình trạng thanh toán !","Thông báo",2);
                    this.cethanhtoan.requestFocus();
                    return;
        }
                
        
        BanSanPham sp = new BanSanPham();
        
        sp.setMaSP(this.txtMaSP.getText());
        sp.setMaDM((String) this.cbMaDMSP.getSelectedItem());
        sp.setTenSP((String) this.cbTenSP.getSelectedItem());  
        sp.setNPPSP((String) this.cbNPPSP.getSelectedItem());
        sp.setDonGia(Float.parseFloat(this.txtdongia.getText()));
        sp.setMaHD(this.txtmahoadon.getText());
        
        sp.setSoLuong(Integer.parseInt(this.txtsoluong.getText()));
        
        sp.setGiaBan(Float.parseFloat(this.txtgiaban.getText()));
        
        String a = this.txtsoluong.getText();
        String b = this.txtgiaban.getText();
        String c = this.txttonggia.getText();
        
        int a1 = Integer.parseInt(a);
        int b1 = Integer.parseInt(b);
        int c1 = Integer.parseInt(c);
        
        c1 = a1 * b1;
        
        String c2 = Integer.toString(c1);
        
        sp.setTongGia(Float.parseFloat(c2));
         
        sp.setNgayBan(this.datengayban.getDate());
  
        String tinhtrang = null;
        if(this.cethanhtoan.isSelected()){
            tinhtrang = "Da thanh toan";
        }
        if(this.ceconno.isSelected()){
            tinhtrang = "Con No";
        }   
        sp.setTinhTrang(tinhtrang);
        
        String maSP = this.txtMaSP.getText();
        boolean kq =  controllerBanSanPham.addDoanhThu(sp,maSP);
//        boolean kq1 = controllerBanSanPham.deleteSanPhamBanDuoc(this.txtMaSP.getText());
        if(kq){
            JOptionPane.showMessageDialog(null, "Đã bán sản phẩm !","Thông báo",1);
            DefaultTableModel model = (DefaultTableModel)tbbansanpham.getModel();
            model.setRowCount(0);
            loadDaTaSanPham();
            refresh();
            
        }else
        {
            JOptionPane.showMessageDialog(null, "Thất bại","Thông báo",2);
        }
    }//GEN-LAST:event_btBanActionPerformed

