    private void btXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaActionPerformed
        // Xóa nhân viên
        
        if(this.txtmnv.getText().length()==0)
            JOptionPane.showMessageDialog(null,"Bạn cần chọn mã khách hàng để xóa","Thông báo",2);
        else
        {   
            int confirmdelete = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa mã nhân viên này không ?"," Thông báo ",JOptionPane.YES_NO_OPTION);
            if (confirmdelete == JOptionPane.YES_NO_OPTION){
                boolean kq = controllerNhanVien.deleteNhanVien(this.txtmnv.getText());
                if(kq){
                    JOptionPane.showMessageDialog(null,"Bạn đã xóa thành công","Thông báo",1);
                    DefaultTableModel model = (DefaultTableModel)tbnhanvien.getModel();
                    model.setRowCount(0);
                    loadDaTaNhanVien();
                    ProcessAction(false);
                    refresh();
                }else{
                    JOptionPane.showMessageDialog(null,"Xóa thất bại !","Thông báo",2);
                }
            }        
        }
    }//GEN-LAST:event_btXoaActionPerformed

