    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        // thêm mới nhân viên
        
        if(this.txtmnv.getText().length()==0){
                    JOptionPane.showMessageDialog(null,"Mã nhân viên không thể bỏ trống","Thông báo",2);
                    this.txtmnv.requestFocus();
                    return;
        }
        
        if(this.txtmnv.getText().length()>10){
                    JOptionPane.showMessageDialog(null,"Mã nhân viên không vượt quá 10 ký tự","Thông báo",2);
                    this.txtmnv.requestFocus();
                    return;
        }
        
        if (this.txthotennv.getText().length()==0) {
                    JOptionPane.showMessageDialog(null, "Mời bạn nhập họ tên nhân viên","Thông báo",2);
                    this.txthotennv.requestFocus();
                    return;
        }
        
        if(this.rdNam.isSelected()==false && this.rdNu.isSelected()==false){
            JOptionPane.showMessageDialog(null,"Mời bạn chọn giới tính","Thông báo",2);
                    this.rdNam.requestFocus();
                    return;
        }
        
        Pattern pattern = Pattern.compile("^[0-9]*$");
        Matcher matcher = pattern.matcher(this.txtsodienthoai.getText());
        if (!matcher.matches()) {
                    JOptionPane.showMessageDialog(null, "Số điện thoại vừa nhập không phải là số!");
                    this.txtsodienthoai.requestFocus();
                    return;
        }
        
        if(this.txtsodienthoai.getText().length()==0)
        {
                JOptionPane.showMessageDialog(null,"Số điện thoại không được bỏ trống","Thông báo",2);
                this.txtsodienthoai.requestFocus();
                return;
        }
        
        if(this.txtsodienthoai.getText().length()>15)
        {
                JOptionPane.showMessageDialog(null,"Số điện thoại vượt quá kí tự cho phép","Thông báo",2);
                this.txtsodienthoai.requestFocus();
                return;
        }
        
        if(this.txtdiachi.getText().length()==0){
                JOptionPane.showMessageDialog(null, "Mời bạn nhập địa chỉ","Thông báo",2);
                this.txtdiachi.requestFocus();
                return;
        }
        
        if(this.txtemail.getText().length()==0){
                JOptionPane.showMessageDialog(null,"Mời bạn nhập email","Thông báo",2);
                this.txtemail.requestFocus();
                return;
        }
        
        String EMAIL_REGEX = "^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
                        + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";
        if (!this.txtemail.getText().matches(EMAIL_REGEX)) {
                    JOptionPane.showMessageDialog(null, "Mời bạn nhập email chính xác");
                    this.txtemail.requestFocus();
                    return;
        }
        
        if(this.txttentk.getText().length()==0)
        {
            JOptionPane.showMessageDialog(null, "Tên tài khoản không được bỏ trống","Thông báo",2);
                    this.txttentk.requestFocus();
                    return;
        }
        
        if(this.txtmk.getText().length()==0)
        {
            JOptionPane.showMessageDialog(null,"Vui lòng nhập mật khẩu","Thông báo",2);
                    this.txtmk.requestFocus();
                    return;
        }
        
        if(this.txtmk.getText().length()<5)
        {
            JOptionPane.showMessageDialog(null,"Mật khẩu nhập quá ngắn","Thông báo",2);
                    this.txtmk.requestFocus();
                    return;
        }
        
        if(this.cbchucvu.getSelectedItem()==null)
            {
                JOptionPane.showMessageDialog(null,"Vui lòng chọn chức vụ","Thông báo",2);
                this.cbchucvu.requestFocus();
                return;
            }
        
        NhanVien nv = new NhanVien();
        
        nv.setMaNV(this.txtmnv.getText());
        nv.setHoTen(this.txthotennv.getText());
        nv.setNgaySinh(this.datengaysinh.getDate());
        
        boolean gioitinh = this.rdNam.isSelected();
        
        if(gioitinh){
            this.rdNam.isSelected();
        }else{
            this.rdNu.isSelected();
        }
        
        nv.setGioiTinh(gioitinh);
        
        nv.setSoDienThoai(Integer.parseInt(this.txtsodienthoai.getText()));
        nv.setDiaChi(this.txtdiachi.getText());
        nv.setEmail(this.txtemail.getText());
        
        nv.setNgayVaolam(this.datengayvaolam.getDate());
        nv.setTenTK(this.txttentk.getText());
        nv.setMatKhau(this.txtmk.getText());
        nv.setChucVu((String) this.cbchucvu.getSelectedItem());
        
        boolean kq =  controllerNhanVien.addNewNhanVien(nv);
        if(kq){
            JOptionPane.showMessageDialog(null, "Thêm nhân viên thành công","Thông báo",1);
            DefaultTableModel model = (DefaultTableModel)tbnhanvien.getModel();
            model.setRowCount(0);
            loadDaTaNhanVien();
            ProcessAction(false);
            refresh();
            
        }else
        {
            JOptionPane.showMessageDialog(null, "Thêm thất bại","Thông báo",2);
        }

    }//GEN-LAST:event_btAddActionPerformed

